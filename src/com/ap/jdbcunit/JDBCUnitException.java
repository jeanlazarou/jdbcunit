package com.ap.jdbcunit;

public class JDBCUnitException extends RuntimeException {

	public JDBCUnitException() {
		super();
	}

	public JDBCUnitException(String message, Throwable cause) {
		super(message, cause);
	}

	public JDBCUnitException(String message) {
		super(message);
	}

	public JDBCUnitException(Throwable cause) {
		super(cause);
	}

}
